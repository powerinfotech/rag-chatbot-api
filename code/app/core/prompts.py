# 근거를 찾지 못했을 때 일관되게 내보내는 거부 메시지(코드/프롬프트 공용).
REFUSAL_MESSAGE = "제공된 판례에서 해당 정보를 찾을 수 없습니다."
REFUSAL_MESSAGE_EN = "The provided case law does not contain information to answer this question."


def _has_korean(text: str) -> bool:
    return any("가" <= ch <= "힣" for ch in text)


def refusal_message_for(question: str) -> str:
    """질문 언어에 맞는 거부 메시지를 돌려준다(한글 포함→한국어, 아니면 영어)."""
    return REFUSAL_MESSAGE if _has_korean(question) else REFUSAL_MESSAGE_EN

